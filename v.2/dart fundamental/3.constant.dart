void main(List<String> args) {
//dengan menggunakan constant kita dapat melindungi nilai yg sudah disimpan dan tidak dapat dimodifikasi/MUTLAK

//keyword final
  final name = 'aji';
  final String myName = 'dwi';
  print(name);
  print(myName);

//keyword const
  const number = 1000;
  const double multiple = 1.5 * number;
  print(multiple);
}
